export const UNIVERSAL_START_MESSAGE =
  MESSAGE_TEMPLATES.find((t) => t.id === "first-short")?.text ?? "";

export function filterMessageTemplates(
  tab: MessageTemplateTabId,
  query: string
): MessageTemplate[] {
  if (tab === "forbidden") return [];

  const normalized = query.trim().toLowerCase();
  return MESSAGE_TEMPLATES.filter((template) => {
    if (template.tab !== tab) return false;
    if (!normalized) return true;

    const haystack = [
      template.title,
      template.whenToUse,
      template.text,
      ...template.tags,
    ]
      .join(" ")
      .toLowerCase();

    return haystack.includes(normalized);
  }).sort((a, b) => a.sortOrder - b.sortOrder);
}
